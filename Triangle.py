# Вычисление S треугольника по формуле Герона
import math
print('Введите длину сторон треугольника через пробел')
a, b, c = map(float, input().split())
p = (a + b + c)/2
s = math.sqrt(p*(p-a)*(p-b)*(p-c))
print(f'Площадь треугольника равна   {s:.2f} см2')
print(f'Периметр треугольника равен  {a + b +c} см')